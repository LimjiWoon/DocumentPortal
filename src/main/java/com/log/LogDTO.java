package com.log;

public class LogDTO {
	private int logWho;
	private String logWhoName;
	private String logWhat;
	private String logWhere;
	private String logWhen;
	private String logHow;
	private String logWhy;
	
	public int getLogWho() {
		return logWho;
	}
	public void setLogWho(int logWho) {
		this.logWho = logWho;
	}
	public String getLogWhoName() {
		return logWhoName;
	}
	public void setLogWhoName(String logWhoName) {
		this.logWhoName = logWhoName;
	}
	public String getLogWhat() {
		return logWhat;
	}
	public void setLogWhat(String logWhat) {
		this.logWhat = logWhat;
	}
	public String getLogWhere() {
		return logWhere;
	}
	public void setLogWhere(String logWhere) {
		this.logWhere = logWhere;
	}
	public String getLogWhen() {
		return logWhen;
	}
	public void setLogWhen(String logWhen) {
		this.logWhen = logWhen;
	}
	public String getLogHow() {
		return logHow;
	}
	public void setLogHow(String logHow) {
		this.logHow = logHow;
	}
	public String getLogWhy() {
		return logWhy;
	}
	public void setLogWhy(String logWhy) {
		this.logWhy = logWhy;
	}
}
